    protected void add(int opcode, String name) {
        CS2Instruction i = new CS2Instruction(opcode);
        i.setName(name);

        assert !instructions.containsKey(opcode);
        instructions.put(opcode, i);

        if (name != null) {
            assert !instructionsByName.containsKey(name);
            instructionsByName.put(name, i);
        }
    }

    public CS2Instruction find(int opcode)
    {
        return instructions.get(opcode);
    }

    public CS2Instruction find(String name)
    {
        return instructionsByName.get(name);
    }

